/** A unit of length measurement */
enum LengthUnit {
    /** US yard */
    YD = "YD",
    /** US foot */
    FT = "FT",
    /** US inch */
    IN = "IN",
    /** US thou (1/1000 inch) */
    THOU = "THOU",
    /** Metric meter */
    M = "M",
    /** Metric decimeter (10 centimeters) */
    DM = "DM",
    /** Metric centimeter */
    CM = "CM",
    /** Metric milimeter */
    MM = "MM",
}
export default LengthUnit;
