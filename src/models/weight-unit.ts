/** A unit of weight measurement */
enum WeightUnit {
    /** US ton */
    TON = "TON",
    /** US pound */
    LBS = "LBS",
    /** US ounce */
    OZ = "OZ",
    /** Metric ton (megagram) */
    MT = "MT",
    /** Metric kilogram */
    KG = "KG",
    /** Metric gram */
    G = "G",
}
export default WeightUnit;
